import TextField from '@mui/material/TextField'
import DesktopDatePicker from '@mui/x-date-pickers/DesktopDatePicker'
import type { NextPage } from 'next'
import Head from 'next/head'
import styles from '../src/styles/Home.module.css'
import { useRouter } from 'next/router'
import { useEffect } from 'react'
import { EntryForm } from '../src/components/EntryForm'
import { DailyEntries } from '../src/components/DailyEntries'
import { Header } from '../src/components/Header'
import { withPageAuthRequired } from '@auth0/nextjs-auth0'
import { useGetDateFromQuery } from '../src/hooks/useGetDateFromQuery'
import { MonthlyOverview } from '../src/components/MonthlyOverview'

const Home: NextPage = () => {
  const { push } = useRouter()
  const { queryDate } = useGetDateFromQuery()

  useEffect(() => {
    if (!queryDate) {
      push({
        query: {
          date: new Date().toDateString(),
        },
      })
    }
  }, [push, queryDate])

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <Header />
        {!!queryDate && (
          <DesktopDatePicker
            label="Date"
            inputFormat="MM/dd/yyyy"
            value={queryDate}
            onChange={(selectedDate) => {
              if (selectedDate) {
                push({
                  query: {
                    date: selectedDate.toDateString(),
                  },
                })
              }
            }}
            renderInput={(params) => <TextField {...params} />}
          />
        )}
        <MonthlyOverview />
        <hr />
        <DailyEntries />
        <EntryForm />
      </main>
    </div>
  )
}

export const getServerSideProps = withPageAuthRequired()

export default Home
